                children: [
                  Icon(Icons.person, size: 16, color: Colors.grey.shade600),
                  const SizedBox(width: 4),
                  Text(
                    user.username,
                    style: TextStyle(fontSize: 12, color: Colors.grey.shade600),
                  ),
                ],
